def render(root: Path) -> dict[Path, bytes]:
    manifest = load_manifest(root)
    models = _validate_models(manifest)
    files: dict[Path, bytes] = {
        Path("plugin.json"): _json_bytes(_plugin_manifest(manifest, "copilot")),
        Path(".claude-plugin/plugin.json"): _json_bytes(_plugin_manifest(manifest, "claude")),
        Path("hooks.json"): _json_bytes(_copilot_hooks()),
        Path("hooks/hooks.json"): _json_bytes(_claude_hooks()),
        Path("compatibility.json"): _json_bytes(_compatibility_report(manifest)),
        Path("scripts/session_start_marker.py"): _session_marker_script(),
    }
    for agent in manifest["agents"]:
        name = agent["name"]
        files[Path(f"generated/copilot/agents/{name}.agent.md")] = _copilot_agent(
            root, agent, models,
        )
        files[Path(f"generated/claude/agents/{name}.md")] = _claude_agent(
            root, agent, models,
        )
    return dict(sorted(files.items(), key=lambda item: item[0].as_posix()))


def compare_outputs(expected: dict[Path, bytes], actual: dict[Path, bytes]) -> list[str]:
    """Return deterministic missing/stale/unexpected projection diagnostics."""
    issues: list[str] = []
    for relative, content in expected.items():
        if relative not in actual:
            issues.append(f"missing generated file: {relative.as_posix()}")
        elif actual[relative] != content:
            issues.append(f"stale generated file: {relative.as_posix()}")
    for relative in sorted(set(actual) - set(expected), key=Path.as_posix):
        issues.append(f"unexpected generated file: {relative.as_posix()}")
    return issues


def _owned_wrapper_files(root: Path) -> dict[Path, Path]:
    owned: dict[Path, Path] = {}
    for relative_directory in OWNED_WRAPPER_DIRECTORIES:
        directory = root / relative_directory
        _require_safe_output_path(
            root, directory, operation="inspect", expected_kind="directory"
        )
        if not directory.is_dir():
            continue
        for candidate in _walk_tree_without_following_links(directory):
            if _is_link_or_junction(candidate):
                relative = candidate.relative_to(root)
                owned[relative] = candidate
            elif candidate.is_file() and candidate.name != ".gitkeep":
                relative = candidate.relative_to(root)
                owned[relative] = candidate
    return owned


def _require_safe_output_path(
    root: Path,
    target: Path,
    *,
    operation: str,
    expected_kind: str,
) -> None:
    try:
        relative = target.relative_to(root)
    except ValueError as exc:
        raise ManifestError(f"generated output escapes plugin root: {target}") from exc

    current = root
    for part in relative.parts[:-1]:
        current = current / part
        if _is_link_or_junction(current):
            raise ManifestError(
                f"refusing to {operation} through a link or junction parent: {current}"
            )
        if current.exists() and not current.is_dir():
            raise ManifestError(f"generated output parent is not a directory: {current}")

    if _is_link_or_junction(target):
        raise ManifestError(
            f"refusing to {operation} through a link or junction: {target}"
        )
    if target.exists():
        if expected_kind == "directory":
            if not target.is_dir():
                raise ManifestError(
                    f"generated wrapper path is not a directory: {target}"
                )
            return
        if expected_kind != "file":
            raise AssertionError(f"unsupported generated output kind: {expected_kind}")
        if not target.is_file():
            raise ManifestError(f"generated output is not a regular file: {target}")
        try:
            link_count = target.stat().st_nlink
        except OSError as exc:
            raise ManifestError(f"cannot inspect generated output: {target}") from exc
        if link_count > 1:
            raise ManifestError(f"refusing to overwrite hardlinked output: {target}")


def _require_safe_write_target(root: Path, target: Path) -> None:
    _require_safe_output_path(
        root, target, operation="write", expected_kind="file"
    )


def _require_safe_check_target(root: Path, target: Path) -> None:
    _require_safe_output_path(
        root, target, operation="inspect", expected_kind="file"
    )


def _atomic_write(root: Path, target: Path, content: bytes) -> None:
    _require_safe_write_target(root, target)
    target.parent.mkdir(parents=True, exist_ok=True)
    _require_safe_write_target(root, target)
    descriptor, temporary_name = tempfile.mkstemp(
        dir=target.parent,
        prefix=f".{target.name}.",
        suffix=".tmp",
    )
    temporary_path = Path(temporary_name)
    try:
        stream = os.fdopen(descriptor, "wb")
        descriptor = -1
        with stream:
            stream.write(content)
            stream.flush()
            os.fsync(stream.fileno())
        os.chmod(temporary_path, 0o644)
        _require_safe_output_path(
            root, temporary_path, operation="write", expected_kind="file"
        )
        _require_safe_write_target(root, target)
        os.replace(temporary_path, target)
    finally:
        if descriptor >= 0:
            os.close(descriptor)
        try:
            os.unlink(temporary_path)
        except FileNotFoundError:
            pass


def check(root: Path) -> list[str]:
    expected = render(root)
    actual: dict[Path, bytes] = {}
    for relative in expected:
        target = root / relative
        _require_safe_check_target(root, target)
        if target.is_file():
            actual[relative] = target.read_bytes()
    for relative, target in _owned_wrapper_files(root).items():
        content = (
            b"<unsafe-link-or-junction>"
            if _is_link_or_junction(target)
            else target.read_bytes()
        )
        actual.setdefault(relative, content)
    return compare_outputs(expected, actual)


def write(root: Path) -> None:
    expected = render(root)
    for relative in expected:
        _require_safe_write_target(root, root / relative)
    owned = _owned_wrapper_files(root)
    for relative, target in owned.items():
        if relative not in expected:
            if _is_link_or_junction(target):
                raise ManifestError(
                    f"refusing to remove unsafe generated link or junction: {target}"
                )
            target.unlink()
    for relative, content in expected.items():
        target = root / relative
        _atomic_write(root, target, content)


